//  Q.6: Write a program to check whether an alphabet is a vowel or consonant.

void main() {
  var alphapet = 'd';

  if (alphapet == 'a' ||
      alphapet == 'e' ||
      alphapet == 'i' ||
      alphapet == 'o' ||
      alphapet == 'u') {
    print('$alphapet is vowel');
  } else {
    print('$alphapet is consonant');
  }
}
